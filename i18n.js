let currentLang = localStorage.getItem('magnom_lang') || 'en';

function t(key) {
    return (I18N[currentLang] && I18N[currentLang][key]) || I18N.en[key] || key;
}

function getLang() {
    return currentLang;
}

function isRtl() {
    return currentLang === 'ar';
}

function parseDbDate(iso) {
    if (!iso) return null;
    try {
        return new Date(iso.replace(' ', 'T'));
    } catch {
        return null;
    }
}

function applyLanguage() {
    const root = document.documentElement;
    root.lang = currentLang;
    root.dir = isRtl() ? 'rtl' : 'ltr';
    document.body?.classList.toggle('rtl-mode', isRtl());
    document.title = t('page.title');

    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (key) el.textContent = t(key);
    });

    document.querySelectorAll('[data-i18n-html]').forEach(el => {
        const key = el.getAttribute('data-i18n-html');
        if (key) el.innerHTML = t(key);
    });

    document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
        const key = el.getAttribute('data-i18n-placeholder');
        if (key) el.placeholder = t(key);
    });

    document.querySelectorAll('[data-i18n-aria]').forEach(el => {
        const key = el.getAttribute('data-i18n-aria');
        if (key) el.setAttribute('aria-label', t(key));
    });

    document.querySelectorAll('.lang-toggle-label').forEach(el => {
        el.textContent = t('lang.switch');
    });

    localStorage.setItem('magnom_lang', currentLang);
    document.dispatchEvent(new CustomEvent('languagechange', { detail: { lang: currentLang } }));
}

function setLanguage(lang) {
    if (!I18N[lang]) return;
    currentLang = lang;
    applyLanguage();
}

function toggleLanguage() {
    setLanguage(currentLang === 'en' ? 'ar' : 'en');
}

function initI18n() {
    applyLanguage();
}

function greetKey() {
    const hour = new Date().getHours();
    if (hour < 12) return 'welcome.morning';
    if (hour < 18) return 'welcome.afternoon';
    return 'welcome.evening';
}

function roleKey(role) {
    if (role === 'admin') return 'role.admin';
    if (role === 'coach') return 'role.coach';
    return 'role.member';
}
